
public class Local_Variable {
	
	
	public void method1() {
		
		
		int a=10;
		System.out.println(a);
	}
	
	public void method2() {
		
		
		int a=20;
		System.out.println(a);
		
	}

	public static void main(String[] args) {
		
		
		Local_Variable l = new Local_Variable();
		l.method1();
		l.method2();

	}

}
